package jcloner;

/**
 * Enum for cloner types.
 * 
 * @author Faruk BOZAN
 * @since 15.5.18
 * @version 15.5.18
 *
 */
public enum ClonerTypes {

	/**
	 * Byte based cloner type.
	 */
	BYTE_BASED_CLONER,

	/**
	 * File based cloner type.
	 */
	FILE_BASED_CLONER,

	/**
	 * Socket based cloner type.
	 */
	SOCKET_BASED_CLONER,

	/**
	 * Invalid cloner type.
	 */
	INVALID_CLONER;

}
